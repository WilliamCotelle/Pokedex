
-- Création de la table pokemon
CREATE TABLE pokemon (
    id SERIAL PRIMARY KEY,
    name VARCHAR(100),
    description TEXT,
    image_url TEXT
);

-- Création de la table type
CREATE TABLE type (
    id SERIAL PRIMARY KEY,
    pokemon_id INT,
    type VARCHAR(50),
    FOREIGN KEY (pokemon_id) REFERENCES pokemon(id)
);

